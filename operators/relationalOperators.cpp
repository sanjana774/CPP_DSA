// IMPLEMENTAION OF RELATIONAL OPERATORS//
#include<iostream>
using namespace std;
int main()
{
    int num1,num2;
    cout<< "First  number is :";
    cin>>num1;

    cout<< "Second  number is :";
    cin>>num2;

    cout<<"OUTPUT :"<<(num1==num2)<<endl;

    cout<<"OUTPUT :"<<(num1!=num2)<<endl;

    cout<<"OUTPUT :"<<(num1<=num2)<<endl;

    cout<<"OUTPUT :"<<(num1>=num2)<<endl;

    cout<<"OUTPUT :"<<(num1>num2)<<endl;

    return 0;

}